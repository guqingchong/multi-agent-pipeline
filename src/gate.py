def _check_integrate(project_name: str) -> Tuple[bool, str]:
    """Run integration-level checks across modules.

    Verifies that cross-module imports are consistent and that
    the project's module graph has no broken dependencies.
    """
    try:
        config = get_config()
    except (ValueError, TypeError, KeyError, RuntimeError, OSError, ConnectionError, TimeoutError, ImportError, AttributeError):
        return False, "Cannot load pipeline config"

    base_dir = config.base_dir
    proj_dir = base_dir / project_name

    if not proj_dir.exists():
        return False, f"Project directory does not exist: {proj_dir}"

    src_dir = proj_dir / config.src_dir_name
    if not src_dir.exists():
        return False, "No source directory"

    py_files = list(src_dir.rglob("*.py"))
    if not py_files:
        return True, "No Python files to integrate"

    # Build module graph and check for broken imports
    module_names: Dict[str, Path] = {}
    for pf in py_files:
        rel = pf.relative_to(src_dir)
        mod_name = str(rel.with_suffix("")).replace("\\", ".").replace("/", ".")
        module_names[mod_name] = pf

    broken_imports: List[str] = []
    for pf in py_files:
        try:
            content = pf.read_text(encoding="utf-8")
        except (OSError, UnicodeDecodeError):
            continue
        # Extract import statements
        import_patterns = re.findall(
            r'^(?:from|import)\s+([a-zA-Z_][a-zA-Z0-9_.]*)',
            content, re.MULTILINE
        )
        for imp in import_patterns:
            # Skip stdlib and third-party; only check project-local
            if imp.startswith(("src.", project_name.lower())):
                # Normalize
                clean = imp
                if clean.startswith("src."):
                    clean = clean[4:]
                # Check each prefix
                parts = clean.split(".")
                for i in range(len(parts), 0, -1):
                    candidate = ".".join(parts[:i])
                    if candidate in module_names:
                        break
                else:
                    broken_imports.append(f"{pf.name}: {imp}")

    if broken_imports:
        return False, f"Broken internal imports: {'; '.join(broken_imports[:5])}"
    return True, "Integration check passed"


def _check_spec_conformance(project_name: str) -> Tuple[bool, str]:
    """Verify conformance between architecture spec and implementation.

    Checks that every module described in specs/architecture.md has a
    corresponding source file, and that key interfaces are implemented.
    """
    try:
        config = get_config()
    except (ValueError, TypeError, KeyError, RuntimeError, OSError, ConnectionError, TimeoutError, ImportError, AttributeError):
        return False, "Cannot load pipeline config"

    base_dir = config.base_dir
    proj_dir = base_dir / project_name

    if not proj_dir.exists():
        return False, f"Project directory does not exist: {proj_dir}"

    arch_path = proj_dir / "specs" / "architecture.md"
    if not arch_path.exists():
        return True, "No architecture.md — skipping spec conformance"

    try:
        arch_content = arch_path.read_text(encoding="utf-8")
    except (OSError, UnicodeDecodeError):
        return True, "Cannot read architecture.md — skipping"

    # Extract module names mentioned in architecture.md
    # Look for code blocks and module references
    module_refs = set(re.findall(
        r'(?:module|component|service|class)\s+[`"]?([a-zA-Z_][a-zA-Z0-9_]*)',
        arch_content, re.IGNORECASE
    ))

    src_dir = proj_dir / config.src_dir_name
    actual_modules: set = set()
    if src_dir.exists():
        for pf in src_dir.rglob("*.py"):
            if pf.name.startswith("__"):
                continue
            actual_modules.add(pf.stem.lower())

    # Check that spec modules have implementations
    unmatched: List[str] = []
    for mod_ref in module_refs:
        mod_lower = mod_ref.lower()
        if mod_lower not in actual_modules:
            # Try snake_case variants
            snake = re.sub(r'(?<!^)(?=[A-Z])', '_', mod_ref).lower()
            if snake not in actual_modules:
                unmatched.append(mod_ref)

    if unmatched:
        return False, f"Spec modules without implementation: {', '.join(unmatched)}"
    return True, "Spec conformance OK"


def _check_evaluate(project_name: str) -> Tuple[bool, str]:
    """Evaluate overall code quality metrics.

    Computes a simple quality score based on:
      - Code/test ratio
      - Documentation presence
      - Code complexity heuristics
    """
    try:
        config = get_config()
    except (ValueError, TypeError, KeyError, RuntimeError, OSError, ConnectionError, TimeoutError, ImportError, AttributeError):
        return False, "Cannot load pipeline config"

    base_dir = config.base_dir
    proj_dir = base_dir / project_name

    if not proj_dir.exists():
        return False, f"Project directory does not exist: {proj_dir}"

    src_dir = proj_dir / config.src_dir_name
    tests_dir = proj_dir / config.tests_dir_name

    src_files = list(src_dir.rglob("*.py")) if src_dir.exists() else []
    test_files = list(tests_dir.rglob("*.py")) if tests_dir.exists() else []

    src_lines = 0
    docstring_count = 0
    for sf in src_files:
        try:
            content = sf.read_text(encoding="utf-8")
        except (OSError, UnicodeDecodeError):
            continue
        src_lines += len(content.splitlines())
        # Count docstrings (triple-quoted strings)
        docstring_count += len(re.findall(r'"""(?:[^"]|"(?!""))*?"""', content, re.DOTALL))
        docstring_count += len(re.findall(r"'''(?:[^']|'(?!''))*?'''", content, re.DOTALL))

    test_lines = sum(
        len(tf.read_text(encoding="utf-8").splitlines())
        for tf in test_files
        if tf.exists()
    ) if test_files else 0

    # Heuristics
    issues: List[str] = []

    min_test_ratio = get_threshold('gate.quality.min_test_ratio', 0.1)
    min_doc_ratio = get_threshold('gate.quality.min_doc_ratio', 0.5)

    # Check test-to-code ratio
    if src_lines > 0:
        ratio = test_lines / src_lines
        if ratio < min_test_ratio:
            issues.append(f"Low test ratio: {ratio:.1%}")
    elif test_lines == 0:
        issues.append("No tests found")

    # Check documentation coverage
    if src_lines > 0:
        doc_ratio = docstring_count / max(src_lines / 50, 1)
        if doc_ratio < min_doc_ratio:
            issues.append(f"Low documentation: {docstring_count} docstrings for {src_lines} LOC")

    if issues:
        return False, f"Quality issues: {'; '.join(issues)}"
    return True, "Evaluation passed"


def _check_inspector_review(project_name: str) -> Tuple[bool, str]:
    """Check that an inspector review has been completed.

    Looks for review artifacts like REVIEW.md or inspector_checkpoint
    in the project's state store.
    """
    try:
        config = get_config()
    except (ValueError, TypeError, KeyError, RuntimeError, OSError, ConnectionError, TimeoutError, ImportError, AttributeError):
        return False, "Cannot load pipeline config"

    base_dir = config.base_dir
    proj_dir = base_dir / project_name

    if not proj_dir.exists():
        return False, f"Project directory does not exist: {proj_dir}"

    # Look for REVIEW.md or similar review artifact
    review_files = list(proj_dir.rglob("REVIEW*"))
    if review_files:
        return True, f"Review artifact found: {review_files[0].name}"

    # Check state store for inspector checkpoint
    db_path = proj_dir / config.db_name
    if db_path.exists():
        try:
            import sqlite3
            conn = sqlite3.connect(str(db_path))
            cur = conn.execute(
                "SELECT COUNT(*) FROM checkpoints WHERE phase LIKE '%review%' OR phase LIKE '%inspector%'"
            )
            count = cur.fetchone()[0]
            conn.close()
            if count > 0:
                return True, f"Inspector checkpoint found ({count} records)"
        except (sqlite3.Error, OSError):
            pass

    return False, "No inspector review found — run inspector before pushing"


