def _check_ai_origin_mark(file_path: Path) -> Tuple[bool, str]:
    """Check that the file contains an AI-origin marker comment.

    Looks for markers like '# AI-generated', '// AI-generated',
    'Generated by AI', or the standard HERMES-AI marker.
    """
    try:
        content = file_path.read_text(encoding="utf-8", errors="replace")
    except (OSError, UnicodeDecodeError):
        return False, f"Cannot read file: {file_path}"

    markers = [
        r"#\s*AI.generated",
        r"//\s*AI.generated",
        r"Generated by AI",
        r"HERMES.AI",
        r"@ai.generated",
        r"@generated",
    ]
    for marker in markers:
        if re.search(marker, content, re.IGNORECASE):
            return True, "AI origin mark found"

    return False, "No AI origin mark found in file"


def _check_hardcoded_secrets(file_path: Path) -> Tuple[bool, str]:
    """Scan for hardcoded secrets like API keys, passwords, tokens.

    Returns (True, ...) if NO secrets are found (i.e. check passes).
    """
    try:
        content = file_path.read_text(encoding="utf-8", errors="replace")
    except (OSError, UnicodeDecodeError):
        return True, "Skipped — cannot read file"

    # Patterns that suggest hardcoded secrets
    min_secret_length = get_threshold("gate.secrets.min_secret_length", 8)
    secret_patterns = [
        (r'api_key\s*=\s*["\'](?!\$\{)[^"\'\s]{' + str(min_secret_length) + r',}["\']', "Hardcoded api_key"),
        (r'password\s*=\s*["\'][^"\']+["\']', "Hardcoded password"),
        (r'secret\s*=\s*["\'][^"\']+["\']', "Hardcoded secret"),
        (r'token\s*=\s*["\'][^"\']{' + str(min_secret_length) + r',}["\']', "Hardcoded token"),
        (r'access_key\s*=\s*["\'](?!\$\{)[^"\'\s]{' + str(min_secret_length) + r',}["\']', "Hardcoded access_key"),
        (r'AWS_SECRET_ACCESS_KEY\s*=\s*["\'][^"\']+["\']', "Hardcoded AWS secret key"),
    ]

    found: List[str] = []
    for pattern, label in secret_patterns:
        if re.search(pattern, content, re.IGNORECASE):
            found.append(label)

    if found:
        return False, f"Hardcoded secrets detected: {', '.join(found)}"
    return True, "No hardcoded secrets found"


def _check_dangerous_patterns(file_path: Path) -> Tuple[bool, str]:
    """Scan for dangerous code patterns: eval, exec, os.system, subprocess shell=True.

    Returns (True, ...) if NO dangerous patterns are found.
    """
    try:
        content = file_path.read_text(encoding="utf-8", errors="replace")
    except (OSError, UnicodeDecodeError):
        return True, "Skipped — cannot read file"

    dangerous = [
        (r'\beval\s*\(', "eval() call"),
        (r'\bexec\s*\(', "exec() call"),
        (r'\bos\.system\s*\(', "os.system() call"),
        (r'\bos\.popen\s*\(', "os.popen() call"),
        (r'shell\s*=\s*True', "subprocess with shell=True"),
        (r'__import__\s*\(\s*["\']os["\']', "dynamic os import"),
        (r'compile\s*\([^)]*\beval\b', "compile() with eval mode"),
        (r'pickle\.loads?\s*\(', "pickle deserialization (potential RCE)"),
    ]

    found: List[str] = []
    for pattern, label in dangerous:
        if re.search(pattern, content):
            found.append(label)

    if found:
        return False, f"Dangerous patterns: {', '.join(found)}"
    return True, "No dangerous patterns found"


def _check_path_safety(file_path: Path) -> Tuple[bool, str]:
    """Check for unsafe path operations: '../' traversal, absolute paths to
    sensitive locations, dynamic path construction without validation.

    Returns (True, ...) if path usage appears safe.
    """
    try:
        content = file_path.read_text(encoding="utf-8", errors="replace")
    except (OSError, UnicodeDecodeError):
        return True, "Skipped — cannot read file"

    unsafe = [
        (r'\.\./\.\./', "Multiple ../ traversal"),
        (r'/etc/passwd', "References /etc/passwd"),
        (r'/etc/shadow', "References /etc/shadow"),
        (r'C:\\Windows\\System32', "References Windows system directory"),
        (r'os\.path\.join\s*\([^)]*\.\./', "Path join with ../"),
        (r'/root/', "References /root/ (privileged)"),
    ]

    found: List[str] = []
    for pattern, label in unsafe:
        if re.search(pattern, content, re.IGNORECASE):
            found.append(label)

    if found:
        return False, f"Unsafe path patterns: {', '.join(found)}"
    return True, "Path usage appears safe"


