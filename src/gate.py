# ───────────────────────────────────────────────────────────────
# GateLevel enum
# ───────────────────────────────────────────────────────────────

class GateLevel(Enum):
    """4-level quality gate, each with a target latency budget."""
    POST_GEN = 0   # <5s — quick scans after code generation
    COMMIT = 1     # <30s — pre-commit quality checks
    PUSH = 2       # <5min — pre-push integration / spec checks
    CI = 3         # CI — comprehensive pipeline validation


# ───────────────────────────────────────────────────────────────
# GateResult dataclass
# ───────────────────────────────────────────────────────────────

@dataclass
class GateResult:
    """Unified result from a gate run.

    Attributes:
        passed: True if all checks in this gate passed.
        level: The GateLevel this result belongs to.
        checks_run: Total number of checks executed.
        checks_passed: Number of checks that passed.
        failures: List of (check_name, reason) tuples for failed checks.
        duration_ms: Wall-clock duration of the gate run in milliseconds.
    """
    passed: bool
    level: GateLevel
    checks_run: int
    checks_passed: int
    failures: List[Tuple[str, str]] = field(default_factory=list)
    duration_ms: float = 0.0


# ───────────────────────────────────────────────────────────────
# Check type alias
# ───────────────────────────────────────────────────────────────

CheckFunc = Callable[..., Tuple[bool, str]]


# ───────────────────────────────────────────────────────────────
# Core: run_checks
# ───────────────────────────────────────────────────────────────

def run_checks(
    checks: List[Tuple[str, CheckFunc, tuple]],
    level: GateLevel,
    timeout: float,
) -> GateResult:
    """Run a list of checks and aggregate results into a GateResult.

    Parameters:
        checks: List of (name, callable, args) tuples.
        level: The GateLevel for reporting / context.
        timeout: Total timeout in seconds (soft — best-effort).

    Returns:
        GateResult with aggregated pass/fail data.
    """
    start = time.perf_counter()
    failures: List[Tuple[str, str]] = []
    checks_run = 0
    checks_passed = 0

    deadline = start + timeout

    for name, func, args in checks:
        if time.perf_counter() > deadline:
            failures.append((name, "Gate timeout exceeded"))
            checks_run += 1
            continue

        try:
            passed, reason = func(*args)
            checks_run += 1
            if passed:
                checks_passed += 1
            else:
                failures.append((name, reason))
        except (ValueError, TypeError, KeyError, RuntimeError, OSError, ConnectionError, TimeoutError, ImportError, AttributeError) as exc:
            checks_run += 1
            failures.append((name, f"Check raised exception: {exc}"))

    elapsed = (time.perf_counter() - start) * 1000.0
    all_passed = len(failures) == 0 and checks_run > 0

    return GateResult(
        passed=all_passed,
        level=level,
        checks_run=checks_run,
        checks_passed=checks_passed,
        failures=failures,
        duration_ms=elapsed,
    )


