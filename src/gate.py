def gate_post_generate(file_path: str) -> GateResult:
    """Run POST_GEN gate checks on a newly generated file.

    Target: <5 seconds. Scans for AI origin markers, hardcoded secrets,
    dangerous patterns, and path safety issues.

    Parameters:
        file_path: Path to the generated file to check.

    Returns:
        GateResult with aggregated pass/fail data.
    """
    path = Path(file_path)
    checks: List[Tuple[str, CheckFunc, tuple]] = [
        ("CheckAIOriginMark", _check_ai_origin_mark, (path,)),
        ("CheckHardcodedSecrets", _check_hardcoded_secrets, (path,)),
        ("CheckDangerousPatterns", _check_dangerous_patterns, (path,)),
        ("CheckPathSafety", _check_path_safety, (path,)),
    ]
    return run_checks(checks, GateLevel.POST_GEN, timeout=get_threshold("gate.timeouts.post_gen", 5.0))


def gate_commit(project_name: str) -> GateResult:
    """Run COMMIT gate checks before committing code.

    Target: <30 seconds. Runs lint, tests, Hermes code leak detection,
    and phase requirement validation.

    Parameters:
        project_name: Name of the project to check.

    Returns:
        GateResult with aggregated pass/fail data.
    """
    checks: List[Tuple[str, CheckFunc, tuple]] = [
        ("CheckFeatureLint", _check_feature_lint, (project_name,)),
        ("CheckFeatureTests", _check_feature_tests, (project_name,)),
        ("CheckNoHermesCode", _check_no_hermes_code, (project_name,)),
        ("CheckPhaseRequirements", _check_phase_requirements, (project_name,)),
    ]
    return run_checks(checks, GateLevel.COMMIT, timeout=get_threshold("gate.timeouts.commit", 30.0))


def gate_push(project_name: str) -> GateResult:
    """Run PUSH gate checks before pushing to remote.

    Target: <5 minutes. Runs integration, spec conformance, evaluation,
    inspector review, all-features-passed, and no-P0-remaining checks.

    Parameters:
        project_name: Name of the project to check.

    Returns:
        GateResult with aggregated pass/fail data.
    """
    checks: List[Tuple[str, CheckFunc, tuple]] = [
        ("CheckIntegrate", _check_integrate, (project_name,)),
        ("CheckSpecConformance", _check_spec_conformance, (project_name,)),
        ("CheckEvaluate", _check_evaluate, (project_name,)),
        ("CheckInspectorReview", _check_inspector_review, (project_name,)),
        ("CheckAllFeaturesPassed", _check_all_features_passed, (project_name,)),
        ("CheckNoP0Remaining", _check_no_p0_remaining, (project_name,)),
    ]
    return run_checks(checks, GateLevel.PUSH, timeout=get_threshold("gate.timeouts.push", 300.0))


def gate_ci(project_name: str) -> GateResult:
    """Run CI gate checks — comprehensive pipeline validation.

    Target: flexible. Runs all PUSH-level checks plus any additional
    CI-specific checks. Currently delegates to gate_push.

    Parameters:
        project_name: Name of the project to check.

    Returns:
        GateResult with aggregated pass/fail data.
    """
    # CI runs the same checks as PUSH but can be extended with additional checks
    result = gate_push(project_name)
    # Re-tag level as CI
    result.level = GateLevel.CI
    return result


# ───────────────────────────────────────────────────────────────
# Convenience: run gate by level name
# ───────────────────────────────────────────────────────────────

_GATE_RUNNERS: Dict[GateLevel, Callable[[str], GateResult]] = {
    GateLevel.POST_GEN: gate_post_generate,
    GateLevel.COMMIT: gate_commit,
    GateLevel.PUSH: gate_push,
    GateLevel.CI: gate_ci,
}


def run_gate(level: GateLevel, target: str) -> GateResult:
    """Run a gate by level with the given target.

    Parameters:
        level: The GateLevel to run.
        target: File path (for POST_GEN) or project name (for others).

    Returns:
        GateResult with aggregated pass/fail data.
    """
    runner = _GATE_RUNNERS.get(level)
    if runner is None:
        return GateResult(
            passed=False,
            level=level,
            checks_run=0,
            checks_passed=0,
            failures=[("run_gate", f"Unknown gate level: {level}")],
        )
    return runner(target)


