"""src/gate.py — 4-level quality gate engine

Implements a 4-level quality gate system that runs checks at different
stages of the pipeline lifecycle:

  GateLevel.POST_GEN (0): <5s  checks on generated files
  GateLevel.COMMIT  (1): <30s checks before committing code
  GateLevel.PUSH    (2): <5min checks before pushing to remote
  GateLevel.CI      (3): CI-level comprehensive checks

Each gate level runs a set of check functions, collects results, and returns
a unified GateResult with pass/fail status, timing, and failure details.
"""

from __future__ import annotations

import re
import time
from dataclasses import dataclass, field
from enum import Enum
from pathlib import Path
from typing import Any, Callable, Dict, List, Optional, Tuple

