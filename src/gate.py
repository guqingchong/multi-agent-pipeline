def _check_feature_lint(project_name: str) -> Tuple[bool, str]:
    """Run a basic lint pass on the project's source files.

    Checks for syntax validity and common code-quality issues.
    This is a lightweight check — no external linter required.
    """
    try:
        config = get_config()
    except (ValueError, TypeError, KeyError, RuntimeError, OSError, ConnectionError, TimeoutError, ImportError, AttributeError):
        return False, "Cannot load pipeline config"

    base_dir = config.base_dir
    proj_dir = base_dir / project_name

    if not proj_dir.exists():
        return False, f"Project directory does not exist: {proj_dir}"

    src_dir = proj_dir / config.src_dir_name
    if not src_dir.exists():
        return False, f"Source directory does not exist: {src_dir}"

    py_files = list(src_dir.rglob("*.py"))
    if not py_files:
        return True, "No Python source files to lint"

    # Basic lint checks
    issues: List[str] = []

    trailing_ws_threshold = get_threshold('gate.lint.trailing_whitespace_threshold', 20)
    long_line_threshold = get_threshold('gate.lint.long_line_threshold', 150)
    long_line_count_threshold = get_threshold('gate.lint.long_line_count_threshold', 5)
    blank_ws_threshold = get_threshold('gate.lint.blank_whitespace_threshold', 10)

    for py_file in py_files:
        try:
            content = py_file.read_text(encoding='utf-8')
        except (OSError, UnicodeDecodeError):
            issues.append(f'Cannot read {py_file.name}')
            continue

        # Check for trailing whitespace on lines
        trailing = [i + 1 for i, line in enumerate(content.splitlines())
                     if line.rstrip() != line]
        if len(trailing) > trailing_ws_threshold:
            issues.append(f'{py_file.name}: {len(trailing)} lines with trailing whitespace')

        # Check for lines > threshold chars
        long_lines = [i + 1 for i, line in enumerate(content.splitlines())
                      if len(line) > long_line_threshold]
        if len(long_lines) > long_line_count_threshold:
            issues.append(f'{py_file.name}: {len(long_lines)} lines exceed {long_line_threshold} chars')

        # Check for blank lines with whitespace
        blank_ws = [i + 1 for i, line in enumerate(content.splitlines())
                    if line.strip() == '' and len(line) > 0]
        if len(blank_ws) > blank_ws_threshold:
            issues.append(f'{py_file.name}: {len(blank_ws)} blank lines contain whitespace')

    if issues:
        return False, f"Lint issues: {'; '.join(issues[:3])}"
    return True, "Lint check passed"


def _check_feature_tests(project_name: str) -> Tuple[bool, str]:
    """Check that test files exist and have basic structural integrity.

    Verifies that each source module has a corresponding test file,
    and that test files contain actual test functions.
    """
    try:
        config = get_config()
    except (ValueError, TypeError, KeyError, RuntimeError, OSError, ConnectionError, TimeoutError, ImportError, AttributeError):
        return False, "Cannot load pipeline config"

    base_dir = config.base_dir
    proj_dir = base_dir / project_name

    if not proj_dir.exists():
        return False, f"Project directory does not exist: {proj_dir}"

    src_dir = proj_dir / config.src_dir_name
    tests_dir = proj_dir / config.tests_dir_name

    src_files = list(src_dir.rglob("*.py")) if src_dir.exists() else []
    test_files = set()
    if tests_dir.exists():
        for tf in tests_dir.rglob("*.py"):
            test_files.add(tf.stem)

    # Check: for each source module, expect a test_<module> or <module>_test
    missing_tests: List[str] = []
    for src_file in src_files:
        if src_file.name.startswith("__"):
            continue
        stem = src_file.stem
        expected_stems = {f"test_{stem}", f"{stem}_test"}
        if not expected_stems & test_files:
            missing_tests.append(stem)

    if missing_tests:
        return False, f"Missing test files for: {', '.join(missing_tests[:5])}"

    # Verify test files have at least one test function
    empty_tests: List[str] = []
    for tf_path in tests_dir.rglob("*.py"):
        try:
            content = tf_path.read_text(encoding="utf-8")
        except (OSError, UnicodeDecodeError):
            continue
        if not re.search(r'def\s+test_', content):
            empty_tests.append(tf_path.name)

    if empty_tests:
        return False, f"Test files without test functions: {', '.join(empty_tests[:3])}"

    return True, f"Tests OK — {len(src_files)} source files, coverage good"


